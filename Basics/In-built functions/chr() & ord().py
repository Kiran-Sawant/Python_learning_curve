#___________________chr()_________________#
"""The chr() function returns the character that represents the specified unicode.
    The following link gives a list of unicode integers. chr() takes integers only
    https://en.wikipedia.org/wiki/List_of_Unicode_characters"""

k = chr(198)
print(k)

#_____________ord()______________#
"""The ord() function returns the number representing the unicode code
    of a specified character."""

j = ord('&')
print(j)